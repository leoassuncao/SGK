public class InstructorsList {

    private int instructor_photo;
    private String instructor_name;
    private String instructor_graduation;
    private String instructor_train;
    private String instructor_classes;
    private String instructor_contact;

    public int getInstructor_photo() {
        return instructor_photo;
    }

    public void setInstructor_photo(int instructor_photo) {
        this.instructor_photo = instructor_photo;
    }

    public String getInstructor_name() {
        return instructor_name;
    }

    public void setInstructor_name(String instructor_name) {
        this.instructor_name = instructor_name;
    }

    public String getInstructor_graduation() {
        return instructor_graduation;
    }

    public void setInstructor_graduation(String instructor_graduation) {
        this.instructor_graduation = instructor_graduation;
    }

    public String getInstructor_train() {
        return instructor_train;
    }

    public void setInstructor_train(String instructor_train) {
        this.instructor_train = instructor_train;
    }

    public String getInstructor_classes() {
        return instructor_classes;
    }

    public void setInstructor_classes(String instructor_classe) {
        this.instructor_classes = instructor_classe;
    }

    public String getInstructor_contact() {
        return instructor_contact;
    }

    public void setInstructor_contact(String instructor_contact) {
        this.instructor_contact = instructor_contact;
    }
}
