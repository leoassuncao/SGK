public class WhereTrainList {

    private int location_image;
    private String location_dojo;
    private String location_gym;
    private String location_public;
    private String location_address;
    private String location_time;

    public int getLocation_image() {
        return location_image;
    }

    public void setLocation_image(int location_image) {
        this.location_image = location_image;
    }

    public String getLocation_dojo() {
        return location_dojo;
    }

    public void setLocation_dojo(String location_dojo) {
        this.location_dojo = location_dojo;
    }

    public String getLocation_gym() {
        return location_gym;
    }

    public void setLocation_gym(String location_gym) {
        this.location_gym = location_gym;
    }

    public String getLocation_public() {
        return location_public;
    }

    public void setLocation_public(String location_public) {
        this.location_public = location_public;
    }

    public String getLocation_address() {
        return location_address;
    }

    public void setLocation_address(String location_address) {
        this.location_address = location_address;
    }

    public String getLocation_time() {
        return location_time;
    }

    public void setLocation_time(String location_time) {
        this.location_time = location_time;
    }
}
